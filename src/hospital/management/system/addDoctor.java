    public boolean UpdateDoctor(String dr_ID, String Dr_name, String str) {
        recep = str;
        String doctorID = dr_ID;
        String name = Dr_name;
        try {
            Connection con = ConnectionProvider.getcon();
            Statement st = con.createStatement();
            ResultSet rs;
            if ("UpdateReceptionist".equals(recep)) {
                rs = st.executeQuery("select * from Receptionists where ReceptionistsID='" + doctorID + "' AND name='" + name + "'");
            } else {
                rs = st.executeQuery("select * from doctors where doctorID='" + doctorID + "' AND name='" + name + "'");
            }
            if (rs.next()) {

                jTextField6.setText(rs.getString(1));
                jTextField1.setText(rs.getString(2));
                jTextField7.setText(rs.getString(3));
                jPasswordField1.setText(rs.getString(4));
                jTextField2.setText(rs.getString(5));
                jTextField4.setText(rs.getString(6));
                jTextField3.setText(rs.getString(8));
                jTextField5.setText(rs.getString(9));
                jTextField6.setEditable(false);
                return true;
            } else {

                JOptionPane.showMessageDialog(null, "PatientID does not exist");
                return false;
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Connection Error");
            return false;
        }

    }

