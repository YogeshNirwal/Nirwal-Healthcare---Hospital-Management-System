    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String doctorID = jTextField6.getText();
        String name = jTextField1.getText();
        String username = jTextField7.getText();
        String password = jPasswordField1.getText();
        String specialization = jTextField2.getText();
        String age = jTextField4.getText();
        String gender = (String) jComboBox1.getSelectedItem();
        String contactNumber = jTextField3.getText();
        String address = jTextField5.getText();

        try {

            Connection con = ConnectionProvider.getcon();
            Statement st = con.createStatement();
            if (null == recep) {

                st.executeUpdate("insert into doctors values('" + doctorID + "','" + name + "','" + username + "','" + password + "','" + specialization + "','" + age + "','" + gender + "','" + contactNumber + "','" + address + "')");
                JOptionPane.showMessageDialog(null, "Successfully Update");
                setVisible(false);
                new addDoctor().setVisible(true);
            } else {
                switch (recep) {
                    case "UpdateDoctor" -> {
                        st.executeUpdate("update doctors set name='" + name + "',username='" + username + "',password='" + password + "',specialization='" + specialization + "',age='" + age + "',gender='" + gender + "',contactNumber='" + contactNumber + "',address='" + address + "' where doctorID='" + doctorID + "'");
                        JOptionPane.showMessageDialog(null, "Successfully Update");
                        setVisible(false);
                        new viewDoctors().setVisible(true);
                    }
                    case "UpdateReceptionist" -> {
                        st.executeUpdate("update Receptionists set name='" + name + "',username='" + username + "',password='" + password + "',qualification='" + specialization + "',age='" + age + "',gender='" + gender + "',contactNumber='" + contactNumber + "',address='" + address + "' where ReceptionistsID='" + doctorID + "'");
                        JOptionPane.showMessageDialog(null, "Successfully Update");
                        setVisible(false);
                        viewDoctors Receptionist = new viewDoctors();
                        Receptionist.jLabel1.setText("RECEPTIONIST DETAILS");
                        Receptionist.setVisible(true);
                        Receptionist.setReceptionistData("Receptionist");
                    }
                    case "Receptionist" -> {
                        st.executeUpdate("insert into Receptionists values('" + doctorID + "','" + name + "','" + username + "','" + password + "','" + specialization + "','" + age + "','" + gender + "','" + contactNumber + "','" + address + "')");
                        JOptionPane.showMessageDialog(null, "Successfully Update");
                        setVisible(false);
                        addDoctor Receptionist = new addDoctor();
                        Receptionist.jLabel1.setText("ADD RECEPTIONIST DETAILS");
                        Receptionist.jLabel3.setText("Qualification*");
                        Receptionist.jLabel7.setText("ID*");
                        Receptionist.setVisible(true);
                        Receptionist.setReceptionistData("Receptionist");
                    }
                    default -> {
                        st.executeUpdate("insert into doctors values('" + doctorID + "','" + name + "','" + username + "','" + password + "','" + specialization + "','" + age + "','" + gender + "','" + contactNumber + "','" + address + "')");
                        JOptionPane.showMessageDialog(null, "Successfully Update");
                        setVisible(false);
                        new addDoctor().setVisible(true);
                    }
                }
            }

        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Enter the uniqe id or fill empty Field");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

